CREATE TABLE BUILDING (
    ID_BUILDING INTEGER NOT NULL,
    NAME VARCHAR(100) NOT NULL,
    ADDRESS VARCHAR(255),
    FLOORS_COUNT INTEGER DEFAULT 1
);

-- Генератор для BUILDING
CREATE GENERATOR GEN_BUILDING_ID;

-- Триггер для BUILDING
SET TERM ^ ;
CREATE TRIGGER TRG_BUILDING_BI FOR BUILDING
ACTIVE BEFORE INSERT POSITION 0
AS
BEGIN
    IF (NEW.ID_BUILDING IS NULL) THEN
        NEW.ID_BUILDING = GEN_ID(GEN_BUILDING_ID, 1);
END^
SET TERM ; ^

